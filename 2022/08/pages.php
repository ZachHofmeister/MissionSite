			<p>
				My team and I are currently in the midst of fall outreach, when we try to meet as many new cadets as possible at Mass and other fun events that we organize. Some of my favorite times with the cadets so far have been leading a bible-study barbecue, cliff jumping, singing karaoke, and playing guitar for a praise and worship Adoration night. The cadets here are amazing people and I am so glad to be entering friendships with many of them already.
			</p>
			<p>
				The bible passage that I included at the top, 1 Thessalonians 2:8, is one that FOCUS holds up as key to sharing our faith, and one that I have been returning to in prayer a lot this summer. I have been blessed with so many friends who have loved me as God does, not superficially but sacrificially. As I am entering into these new friendships, my prayer is that I will be able to show the same self-giving love to these men and women as has been shown to me.
